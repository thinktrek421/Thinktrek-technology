                            <p><b>1. What is Legal Metrology?</b></p>
                            <p>Legal Metrology treats units of weighment and measurement, methods of weighment 
                            and measurement and weighing and measuring instruments, in relation to the 
                            mandatory technical and legal requirements which have the object of ensuring public 
                            guarantee from the point of view of security and accuracy of the weighments and 
                            measurements.</p><br>
                            <p><b>2. Whether the offences committed under the packaged commodities
                            Rules are compoundable?</b></p>
                            <p>Yes, the offences committed under the packaged commodities Rules are 
                            compoundable.</p><br>
                            <p><b>3. What is Pre-packaged commodity?</b></p>
                            <p>“Pre-packaged commodity” means a commodity which without the purchaser being 
                            present is placed in a package of whatever nature, whether sealed or not, so that 
                            the product contained therein has a pre-determined quantity.</p><br>
                            <p><b>4. What do you mean by sale?</b></p>
                            <p>Sale means transfer of Property in any weight, measure or other goods by one person 
                            to another for cash or for deferred payment or for any other valuable consideration 
                            and includes a transfer of any weight, measure or other goods on the hire-purchase 
                            system or any other system of payment by installments, but does not include a 
                            mortgage or hypothecation of, or a charge or pledge on, such weight, measure or 
                            other goods.</p><br>
                            <p><b>5. How the transaction has been defined in the Legal Metrology Act?</b></p>
                            <p>Any contract, whether for sale, purchase, exchange or any other purpose, or
                            Any assessment of royalty, toll, duty or other dues, or
                            The assessment of any work done, wages due or services rendered.</p><br>
                            <p><b>6. What do you mean by protection in Legal Metrology?</b></p>
                            <p>“Protection” means the utilization of reading obtained from any weight or measure, 
                            for the purpose of determining any step which is required to be taken to safeguard 
                            the well-being of any human being or animal, or to protect any commodity, 
                            vegetation or thing, whether individually or collectively.</p><br>
                            <p><b>7. What do you mean by label?</b></p>
                            <p>“Label” means any written, marked, stamped, printed or graphic matter affixed to, 
                            or appearing upon any pre-packaged commodity.</p><br>
                            <p><b>8. What is net quantity?</b></p>
                            <p>Net quantity in relation to commodity contained in a package, means the quantity by 
                            weight, measure or number of such commodity contained in that package, excluding 
                            the packaging or wrappers.</p><br>
                            <p><b>9. What is Principal display panel?</b></p>
                            <p>In relation to a package means the total surface area of package where the 
                            information required under these rules are to be given in the following manner:
                            (i) All the information could be grouped together and given at one place. <br>
                            (ii) The pre-printed information could be grouped together and given in one place. and on line information grouped together in other place.</p><br>
                            <p><b>10. What is retail dealer?</b></p>
                            <p>“retail dealer” in relation to any commodity in packaged from means a dealer who 
                            directly sells such packages to the consumer and includes, in relation to packages as 
                            are sold directly to the consumer, a wholesale dealer who makes such direct sale to 
                            the consumer.</p><br>
                            <p><b>11. What do you mean by an Institutional consumer?</b></p>
                            <p>“Institutional consumer” means the institution which buys packaged commodities 
                            bearing a declaration ‘not for retail sale’, directly from the manufacturer or from an 
                            importer or from wholesale dealer for use by that institution and not for commercial 
                            or trade purposes.</p><br>
                            <p><b>12. What do you mean by an industrial consumer?</b> </p>
                            <p>“Industrial consumer” means the consumer who buys packaged commodities directly 
                            from the manufacturer or from an importer or from wholesale dealer for use by that 
                            industry and the package shall have declaration ‘not for retail sale.</p><br>
                            <p><b>13. What is retail sale price?</b> </p>
                            <p>Retail sale Price means the maximum price at which the commodity in packaged form 
                            may be sold to the ultimate consumer and the price shall be printed on the package 
                            in the form of Maximum Retail Price.</p><br>
