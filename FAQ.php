                            in number separately. Am I covered under the Legal Metrology 
                            (Packaged Commodities) Rules, 2011?</b></p>
                            <p>No.</p><br>

                            <p><b>18. Whether the Rules are applicable to imported packages?</b> </p>
                            <p>Yes, the Rules are applicable both to imported packages as well as the indigenous 
                            packages.</p><br>

                            <p><b>19. Whether additional sticker can be affixed on the package to provide 
                            information required under the Rules?</b></p>
                            <p>The Rules provide that all the information required under the Rules either printed on 
                            the package itself or on a label affixed thereto. Therefore, given all the information 
                            on a single label affixed on the package is not prohibited. However, giving individual 
                            information likes date of manufacture or retail sale price etc. by affixing individual 
                            sticker is prohibited under Rule 6(2). However affixing individual sticker is not 
                            prohibited for declaring reduced MRP provided that the MRP declaration made by the 
                            manufacture shall not covered.</p><br>

                            <p><b>20. Whether giving additional information is considered violation under 
                            the Packaged Commodities Rules?</b> </p>
                            <p>Giving any information in addition to the mandatory information required under the 
                            Rules is not considered violation.</p><br>
                            <p><b>21. Who is responsible in case of imported packaged?</b> </p>
                            <p>Registered Importer, in India, is responsible in case of imported packaged.</p><br>
                            <p><b>22. Whether loose commodities are covered under the PC Rules?</b> </p>
                            <p>No, Rules covered only pre-packaged commodities.</p><br>
                            <p><b>23. Whether there is provision to sell a commodity at a price lower than MRP?</b></p>
                            <p>Yes, under sub-rule (3) of rule 6 of the Legal Metrology (Packaged Commodities) Rules, 2011 a commodity may be sold at a price lower than MRP.</p><br>
                            <p><b>24. The price declared on a pre-packaged commodity can be changed in 
                            due course?</b> </p>
                            <p>No, under sub-rule (5) and (6) of Rule 18 of the Legal Metrology (Packaged 
                            Commodities) Rules, 2011 no one can alter the price once printed.</p><br>
                            <p><b>25. Whether the packaged commodities can be sold at a price higher 
                            than MRP?</b> </p>
                            <p>No, under Rule 18(2) of the Rules ‘No retail dealer or other person including manufacturer, packer, importer and wholesale dealer shall make any sale of any commodity in packed form at a price exceeding the retail sale price thereof’.</p><br>
                            <p><b>26. What are the commodities covered under the Rules?</b></p>
                            <p>All pre-packaged commodities except otherwise exempted from the rules are covered under the Rules. The exemption is given for Drugs, fast food items and packaged commodities sold in the packages upto 10g or 10ml.</p><br>
                            <p><b>27. What are main declarations required under the Rules?</b></p>
                            <p>The following declarations are required to be given on all pre-packaged commodities meant for retail sale:<br>
                            i. Name and address of manufacturer/ packer & manufacturer (if manufacturer 
                            is not packer)/ importer<br>
                            ii. Country of origin if imported<br>
                            iii. Common, generic name of the commodity<br>
                            iv. Net qty. in std. unit of W or M or no. of commodity in the package.<br>
                            v. MRP incl. of all taxes.<br>
                            vi. Unit sale price (w.e.f. 01.10.2022).<br>
                            vii. Month and year of manufacture *(or pre-packing or import) (except food 
                            articles, Seeds and cosmetics) (*Omit w.e.f. 01.10.2022).<br>
                            viii. “Best before or use by date, month and year” in case of commodity becoming unfit for human consumption with time.<br>
                            ix. Consumer Care details</p><br>
                            <p><b>28. Whether e-mail address is mandatory?</b> </p>
                            <p>Yes, e-mail address is mandatory.</p><br>
                            <p><b>29. What is the area prescribed for Principal Display panel for 
                            declaration?</b></p>
                            <p>The area not including the top, bottom, flange at top and bottom of cans, and shoulders and neck of bottle and jar shall be determined as follows:
                            a. In the case of a rectangular package, where one entire side can properly be considered to be the principal display panel side, the product of the height multiplies by the width of that side.<br>
                            b. In the case of a cylindrical or nearly cylindrical package, 40 percent of the product of the height of the package multiplied by the circumference.<br>
                            c. In the case of any other shaped package, 40 percent of the total surface of the package, or an area considered to be a principle display panel of the package.</p><br>
                            <p><b>30. Whether it is mandatory that a label should be affixed covering the entire 40% of the total surface area earmarked for the Principal Display Panel (PDP)?</b></p>
                            <p>No. There is no requirement that there shall be a label pasted on the package covering the entire PDP area. The Principal Display Panel denotes the area, which is 40% of the total surface area in case of cylindrical surfaces excluding top, bottom and flanges, where all the mandatory declarations have to be made. All the information. <br>
                            (a) can be grouped together and given at one place. <br> 
                            (b) the pre-printed information of declaration can be grouped together at one place and online information can be grouped together at a different place. Both sets of information however, should be printed in a font size as prescribed in the rules.</p><br>

                            <p><b>31. Whether the left over space of the Principal Display Panel may be 
                            used for other declarations?</b></p>
                            <p>Yes, Principal Display Panel prescribes where the mandatory declaration is to be given 
                            and does not restrict the right of the manufacturer / packer / importer to utilize the 
                            left over space for other declarations / promotions. </p><br>

                            <p><b>32. What is unit sale price?</b></p>
                            <p>Unit sale price means the sale price (inclusive of all taxes) per specified unit of weight, measure or number. </p><br>

                            <p><b>33. Where the unit sale price should be declared?</b></p>
                            <p>The unit sale price shall be declared on the principal display panel of the prepackaged commodity. </p><br>
                            <p><b>34. What is the number of decimal places which need to be indicated in 
                            the declaration of unit sale price? </b></p>
                            <p>As per Rule 6, sub-rule 11, the unit sale price shall be rounded off to the nearest 
                            two decimal places.
                            In case where the unit sale price is a whole number, it may be declared without 
                            decimal places.</p><br>

                            <p><b>35. Is unit sale price required to be declared on wholesale packages?</b> </p>
                            <p>Unit sale price is not required to be declared on a ‘Wholesale Package’, defined 
                            under Rule 2(r) of the Legal Metrology (Packaged Commodities) Rules, 2011.</p><br>

                            <p><b>36. Is the unit sale price required to be declared in advertisements?</b></p>
                            <p>The requirement of declaring the unit sale price is limited to product itself. As 
                            per Rule 31(1), in an advertisement with the MRP, net quantity needs to be 
                            declared.</p><br>

                            <p><b>37. Is unit sale price required to be displayed on e-commerce website?</b></p>
                            <p>As per Rule 6 sub-rule 10 of the Legal Metrology (Packaged Commodities) 
                            Rules, 2011, the e-commerce entity is required to ensure mandatory declaration as specified in Rule 6 sub-rule 1. Accordingly, the unit sale price is not required to be displayed on e-commerce websites.</p><br> 

                            <p><b>38. Is Unit sale price required to be declared if it is equal to the MRP?</b></p> 
                            <p>As per the second proviso to Rule 6 sub-rule (11) declaration of unit sale price is not required for the pre-packaged commodities in which retail sale price or MRP is equal to the unit sale price.</p><br>

                            <p><b>39. Is unit sale price required to be declared on the inner package, if the 
                            outer package contains such declaration required under this rule?</b></p>
                            <p>As per the proviso of Rule 9(3), no declarations on the inner package is 
                            required, the outer package contains all declarations required under these 
                            rules.</p><br>
                            <p><b>40. Is unit sale price required to be declared on packs offering additional volume/quantity/units for FREE inside the primary pack?</b></p>
                            <p>The Unit Sale Price is required to be declared, however, the Unit Sale Price computation shall exclude the additional volume/ quantity/ units being provided for FREE.</p><br>

