                        <div><h2 class="page-heading">Fssai Certificate</h2></div>
                        <p class="pt-2 text-dark">The Food Safety and Standards Authority of India (FSSAI) is a regulatory body that governs food safety in India. To operate a food business in India, it is mandatory for businesses to obtain an FSSAI License or Registration. Here's a breakdown of the FSSAI License Registration process:</p>
                        <h2 class="page-heading">FSSAI License Registration Process</h2>
                        <p class="pt-2 text-dark"> <b>1. Determine the Type of FSSAI License Required.</b><br>
                        <p class="pt-2 text-dark">FSSAI categorizes food businesses based on their scale and nature. The type of license required depends on the size of the business:
                            <p class="pt-2 text-dark"><b>• FSSAI Basic Registration:</b> For small businesses with annual turnover below ₹12 lakhs (e.g., small food manufacturers, retailers, or food vendors).</p>
                            <p class="pt-2 text-dark"><b>•  FSSAI State License: </b>For medium-sized businesses with annual turnover between ₹12 lakhs and ₹20 crores (e.g., restaurants, medium-sized food manufacturers, etc.).</p>
                            <p class="pt-2 text-dark"><b>•  FSSAI Central License: </b>For large businesses with annual turnover above ₹20 crores (e.g., large food processing units, importers, exporters, etc.).</p>
                        </p></p>

                        <p class="pt-2 text-dark"> <b>2. Application Preparation.</b><br>
                        <p class="pt-2 text-dark">Once the business type is identified, the applicant must gather the necessary documents. These may include:
                            <p class="pt-2 text-dark"><b>•  Identity Proof </b> (Aadhaar card, PAN card, etc.)</p>
                            <p class="pt-2 text-dark"><b>•  Address Proof </b> (Rent agreement, property ownership documents, etc.)</p>
                            <p class="pt-2 text-dark"><b>•  Business Establishment Proof </b>(Company registration documents, GST certificate, etc.)</p>
                            <p class="pt-2 text-dark"><b>•  Food Safety Management System Plan  </b> (if required)</p>
                            <p class="pt-2 text-dark"><b>•  List of Food Products  </b>to be handled (for manufacturers, producers)</p>
                            <p class="pt-2 text-dark"><b>•  Details of Key Personnel   </b>(e.g., food safety supervisor)</p>
                        </p></p>

                        <p class="pt-2 text-dark"> <b>3. Register on the FSSAI Portal</b><br>
                            <p class="pt-2 text-dark">• Visit the FSSAI official website: www.fssai.gov.in</p>
                            <p class="pt-2 text-dark"><b>• Sign </b> up for a new account or <b>log</b> in if you already have an account.</p>
                            <p class="pt-2 text-dark">• After logging in, select the appropriate license type (Basic Registration, State License, or Central License).</p>
                        </p>

                        <p class="pt-2 text-dark"> <b>4. Fill the Application Form</b><br>
                            <p class="pt-2 text-dark">• The applicant must fill in the details of the food business on the FSSAI portal.</p>
                            <p class="pt-2 text-dark">• Basic details like business name, business address, nature of the business, and contact details will be required.</p>
                            <p class="pt-2 text-dark">• You may also need to provide information related to the type of food products and processes involved.</p>
                        </p>

                        <p class="pt-2 text-dark"> <b>5. Submit Documents</b><br>
                            <p class="pt-2 text-dark">• Upload the necessary documents as per the business type.</p>
                            <p class="pt-2 text-dark">• Depending on the business, the documents may include:<br><br>
                                  - Ownership/lease/rental agreement<br>
                                  - Partnership deed or Memorandum of  Association (for companies)<br>
                                  - GST registration or PAN card<br>
                                  - Food safety management plan (if applicable)<br>
                             </p>
                        </p>

                         <p class="pt-2 text-dark"> <b>6. Fees Payment</b><br>
                            <p class="pt-2 text-dark">• Once the application is filled and documents are submitted, you will be required to pay the application fee.</p>
                            <p class="pt-2 text-dark">• The fee varies based on the type of license and the size of the business. <br><br>
                                  - <b>Basic Registration Fee:</b> Typically low.<br>
                                  - <b>State License Fee:</b> Based on the turnover and scale of the business.<br>
                                  - <b>Central License Fee:</b> For large-scale businesses, and generally higher.<br>
                             </p>
                        </p>

                        <p class="pt-2 text-dark"> <b>7. Inspection (If Required)</b><br>
                            <p class="pt-2 text-dark">• For certain applications, FSSAI may conduct an inspection of the business premises.</p>
                            <p class="pt-2 text-dark">• The inspection checks for compliance with food safety regulations and the proper maintenance of hygiene. <br>
                             </p>
                        </p>

                        <p class="pt-2 text-dark"> <b>8. FSSAI License Issuance</b><br>
                            <p class="pt-2 text-dark">• Once your application is processed and, if applicable, the inspection is completed successfully, the FSSAI will issue the license or registration.</p>
                            <p class="pt-2 text-dark">• The license will be valid for <b>1 to 5 years,</b> depending on the validity period selected during the application process<br>
                             </p>
                             <p class="pt-2 text-dark">• The license number will be issued, and this number must be displayed on all food products sold under the license<br>
                             </p>
                        </p>

                        <p class="pt-2 text-dark"> <b>9. Display FSSAI Logo & License Number</b><br>
                            <p class="pt-2 text-dark">• The FSSAI logo and the 14-digit license number must be displayed on food products and packaging. This ensures that consumers can trust that the product has passed the necessary safety regulations.</p>
                        </p>

                        <p class="pt-2 text-dark"> <b>Key Points to Remember:</b><br>
                            <p class="pt-2 text-dark"><b>• Renewal of License:</b> FSSAI licenses are valid for 1 to 5 years, after which renewal is required.</p>
                            <p class="pt-2 text-dark"><b>•  Penalties for Non-Compliance</b>Operating without a valid FSSAI License is an offense and can lead to penalties or suspension of business.</p>
                            <p class="pt-2 text-dark"><b>• Food Safety Compliance: </b>All businesses must comply with food safety regulations and maintain hygiene standards as prescribed by FSSAI.</p>
                        </p>
